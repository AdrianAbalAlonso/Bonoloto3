    private void bValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bValidarActionPerformed

        int aciertos = 0;
        aleatorios();
        TValidar.setText(validar.toString());

        for (int i = 0; i < validar.size(); i++) {
            validar.get(i);
            for (int j = 0; j < numConcur.size(); j++) {
                if (validar.get(i) == numConcur.get(j)) {
                    aciertos++;
                }
            }
            TAciertos.setText(Integer.toString(aciertos)+" Aciertos");
        }
        if (aciertos==validar.size()) {
            jTextField1.setBackground(Color.green);

            jTextField1.setText("Has ganado");
        } else {

            jTextField1.setText("Has Perdido");
        }
    }//GEN-LAST:event_bValidarActionPerformed

